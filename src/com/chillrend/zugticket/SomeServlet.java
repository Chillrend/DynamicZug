    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws IOException {
        
        zugProcess zugg = new zugProcess();
        
        String selectedOptions = request.getParameter("key");
        String query = request.getParameter("target");
        String identifier = request.getParameter("identifier");
        zugg.setSelectedOption(selectedOptions);
        zugg.setQuery(query);
        zugg.setIdentifier(identifier);
        
        zugg.changeSelect();
        
        Map<String, Object> options = zugg.getSelectData();
        
        String json = new Gson().toJson(options);

        response.setContentType("application/json");
        response.setCharacterEncoding("UTF-8");
        response.getWriter().write(json);
    }

